plain_text = input("Enter the plain text: ")
k = int(input("Enter the number of permutations for the encryption: "))
caesar = Caesar(k)

enc_text = caesar.compute(0, plain_text)
print(f"The encrypted message is: {enc_text}")

dec_text = caesar.compute(1, enc_text)
print(f"The decrypted message is: {dec_text}")