class Caesar:
  def __init__(self, permutations):
    self.permutations = permutations

  @staticmethod
  def normal_input(text):
    return text.lower()

  def encrypt(self, message):
    enc_message = ""
    for char in message:
      if char.isalpha():
        enc_message += chr((ord(char) + self.permutations - 97) % 26 + 97)
    return enc_message

  def decrypt(self, message):
    dec_message = ""
    for char in message:
      if char.isalpha():
        dec_message += chr((ord(char) - self.permutations - 97) % 26 + 97)
    return dec_message

  def compute(self, option, message):
    if option == 0:
      return self.encrypt(self.normal_input(message))
    elif option == 1:
      return self.decrypt(self.normal_input(message))

